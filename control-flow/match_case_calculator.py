num1 = int (input("Enter the first number:" ) )
num2 = int(input("Enter the second number:"))
operation = input("Choose the operation (+, -, *, /):")
if operation == "+" :
    print(f" The_result_is : {num1 + num2}")
elif operation == "-":
    print(f" the result is :{num1 - num2}" )  
elif operation == "*"  :
    print(f"the result is :{num1 *num2}" )
elif operation == "/" :
    print(f"the result is :{num1 / num2}")

     
    
